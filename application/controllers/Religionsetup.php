<?php

if (!defined('BASEPATH'))
    exit('No direct script access allowed');

class Religionsetup extends CI_Controller
{
    function __construct()
    {
        parent::__construct();
        $this->load->model('Religion_model');
        $this->load->library('form_validation');
        $this->load->library('datatables');
    }

    public function index()
    {
        if (isExistClassMenu() == FALSE) {
            redirect(site_url('dashboard'));
        }

        $this->template->general('religionsetup/emp_religion_list');
    }

    public function json()
    {
        header('Content-Type: application/json');
        echo $this->Religion_model->json();
    }

    public function read($id)
    {
        if (isExistClassMenu() == FALSE) {
            redirect(site_url('dashboard'));
        }

        $row = $this->Religion_model->get_by_id($id);
        if ($row) {
            $data = array(
                'button' => 'Read',
                'action' => 'read',
                'id' => $row->id,
                'religion_desc' => $row->religion_desc,
            );
            $this->template->general('religionsetup/emp_religion_form', $data);
        } else {
            $this->session->set_flashdata('message', 'Record Not Found');
            redirect(site_url('religionsetup'));
        }
    }

    public function create()
    {
        if (isExistClassMenu() == FALSE) {
            redirect(site_url('dashboard'));
        }

        $data = array(
            'button' => 'Create',
            'action' => site_url('religionsetup/create_action'),
            'id' => set_value('id'),
            'religion_desc' => set_value('religion_desc'),
        );
        $this->template->general('religionsetup/emp_religion_form', $data);
    }

    public function create_action()
    {
        if (isExistClassMenu() == FALSE) {
            redirect(site_url('dashboard'));
        }

        $this->_rules();

        if ($this->form_validation->run() == FALSE) {
            $this->create();
        } else {
            $data = array(
                'religion_desc' => $this->input->post('religion_desc', TRUE),
            );

            $this->Religion_model->insert($data);
            $this->session->set_flashdata('message', 'Create Record Success');
            redirect(site_url('religionsetup'));
        }
    }

    public function update($id)
    {
        if (isExistClassMenu() == FALSE) {
            redirect(site_url('dashboard'));
        }

        $row = $this->Religion_model->get_by_id($id);

        if ($row) {
            $data = array(
                'button' => 'Update',
                'action' => site_url('religionsetup/update_action'),
                'id' => set_value('id', $row->id),
                'religion_desc' => set_value('religion_desc', $row->religion_desc),
            );
            $this->template->general('religionsetup/emp_religion_form', $data);
        } else {
            $this->session->set_flashdata('message', 'Record Not Found');
            redirect(site_url('religionsetup'));
        }
    }

    public function update_action()
    {
        if (isExistClassMenu() == FALSE) {
            redirect(site_url('dashboard'));
        }

        $this->_rules();

        if ($this->form_validation->run() == FALSE) {
            $this->update($this->input->post('id', TRUE));
        } else {
            $data = array(
                'religion_desc' => $this->input->post('religion_desc', TRUE),
            );

            $this->Religion_model->update($this->input->post('id', TRUE), $data);
            $this->session->set_flashdata('message', 'Update Record Success');
            redirect(site_url('religionsetup'));
        }
    }

    public function delete($id)
    {
        if (isExistClassMenu() == FALSE) {
            redirect(site_url('dashboard'));
        }

        $row = $this->Religion_model->get_by_id($id);

        if ($row) {
            $this->Religion_model->delete($id);
            $this->session->set_flashdata('message', 'Delete Record Success');
            redirect(site_url('religionsetup'));
        } else {
            $this->session->set_flashdata('message', 'Record Not Found');
            redirect(site_url('religionsetup'));
        }
    }

    public function _rules()
    {
        $this->form_validation->set_rules('religion_desc', 'religion desc', 'trim|required');

        $this->form_validation->set_rules('id', 'id', 'trim');
        $this->form_validation->set_error_delimiters('<span class="text-danger">', '</span>');
    }
}

