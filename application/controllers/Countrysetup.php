<?php

if (!defined('BASEPATH'))
    exit('No direct script access allowed');

class Countrysetup extends CI_Controller
{
    function __construct()
    {
        parent::__construct();
        $this->load->model('Country_model');
        $this->load->library('form_validation');
        $this->load->library('datatables');
    }

    public function index()
    {
        if (isExistClassMenu() == FALSE) {
            redirect(site_url('dashboard'));
        }

        $this->template->general('countrysetup/country_list');
    }

    public function json()
    {
        header('Content-Type: application/json');
        echo $this->Country_model->json();
    }

    public function read($id)
    {
        if (isExistClassMenu() == FALSE) {
            redirect(site_url('dashboard'));
        }

        $row = $this->Country_model->get_by_id($id);
        if ($row) {
            $data = array(
                'button' => 'Read',
                'action' => 'read',
                'id' => $row->id,
                'country' => $row->country,
            );
            $this->template->general('countrysetup/country_form', $data);
        } else {
            $this->session->set_flashdata('message', 'Record Not Found');
            redirect(site_url('countrysetup'));
        }
    }

    public function create()
    {
        if (isExistClassMenu() == FALSE) {
            redirect(site_url('dashboard'));
        }

        $data = array(
            'button' => 'Create',
            'action' => site_url('countrysetup/create_action'),
            'id' => set_value('id'),
            'country' => set_value('country'),
        );
        $this->template->general('countrysetup/country_form', $data);
    }

    public function create_action()
    {
        if (isExistClassMenu() == FALSE) {
            redirect(site_url('dashboard'));
        }

        $this->_rules();

        if ($this->form_validation->run() == FALSE) {
            $this->create();
        } else {
            $data = array(
                'country' => $this->input->post('country', TRUE),
            );

            $this->Country_model->insert($data);
            $this->session->set_flashdata('message', 'Create Record Success');
            redirect(site_url('countrysetup'));
        }
    }

    public function update($id)
    {
        if (isExistClassMenu() == FALSE) {
            redirect(site_url('dashboard'));
        }

        $row = $this->Country_model->get_by_id($id);

        if ($row) {
            $data = array(
                'button' => 'Update',
                'action' => site_url('countrysetup/update_action'),
                'id' => set_value('id', $row->id),
                'country' => set_value('country', $row->country),
            );
            $this->template->general('countrysetup/country_form', $data);
        } else {
            $this->session->set_flashdata('message', 'Record Not Found');
            redirect(site_url('countrysetup'));
        }
    }

    public function update_action()
    {
        if (isExistClassMenu() == FALSE) {
            redirect(site_url('dashboard'));
        }

        $this->_rules();

        if ($this->form_validation->run() == FALSE) {
            $this->update($this->input->post('id', TRUE));
        } else {
            $data = array(
                'country' => $this->input->post('country', TRUE),
            );

            $this->Country_model->update($this->input->post('id', TRUE), $data);
            $this->session->set_flashdata('message', 'Update Record Success');
            redirect(site_url('countrysetup'));
        }
    }

    public function delete($id)
    {
        if (isExistClassMenu() == FALSE) {
            redirect(site_url('dashboard'));
        }

        $row = $this->Country_model->get_by_id($id);

        if ($row) {
            $this->Country_model->delete($id);
            $this->session->set_flashdata('message', 'Delete Record Success');
            redirect(site_url('countrysetup'));
        } else {
            $this->session->set_flashdata('message', 'Record Not Found');
            redirect(site_url('countrysetup'));
        }
    }

    public function _rules()
    {
        $this->form_validation->set_rules('country', 'country', 'trim|required');

        $this->form_validation->set_rules('id', 'id', 'trim');
        $this->form_validation->set_error_delimiters('<span class="text-danger">', '</span>');
    }
}

